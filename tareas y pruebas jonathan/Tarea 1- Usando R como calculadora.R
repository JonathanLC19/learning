#¿a qué hora de qué día de qué año llegaríamos a los 250 millones de segundos?
#¡Cuidado con los años bisiestos!
  
  #Segundos
250000000

#Minutos
  250000000/60
  4166667
  #Horas
  4166667%/%60 #69444 horas
  4166667%%60 #27 minutos
  
  69444%/% 24 #2893 días
  69444%%24   #12 horas
  
  2893%/% 365 #7 años (2 bisiestos)
  (2893%%365)-2 #336 días
  365-336
2018+7  
